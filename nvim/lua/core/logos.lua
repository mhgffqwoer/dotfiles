---@param type? "dragon" | "week_day"
function M.GetLogo(type)
	if type == "dragon" then
		return vim.split(dragon, "\n")
	end
	if type == "week_day" then
		local current_day = os.date("%A")
		current_day = days_translation[current_day]
		return vim.split("\n\n\n" .. os.date("%d.%m.%Y" .. "\n\n") .. week_days[current_day] .. "\n", "\n")
	end
	return vim.split("\n" .. "aaa", "\n")
end

